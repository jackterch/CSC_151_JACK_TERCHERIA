public class WhileLoop {

    private static void executeWhileLoop() {
        int number = 0;

        while (number <= 9) 
        {
            System.out.println("execute 10 times");
            number++;
        }

        System.out.println(number + " iterations");
        System.out.println("That is all");
    }

    public static void CallExecuteWhileLoop() {
        executeWhileLoop();
    }
}
